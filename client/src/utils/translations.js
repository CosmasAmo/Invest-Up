// Function to get a translation
export const getTranslation = (key, language = 'english', fallback = null) => {
  if (!translations[language]) {
    console.warn(`Language "${language}" not found, falling back to English`);
    language = 'english';
  }
  
  if (!translations[language][key]) {
    console.warn(`Translation key "${key}" not found in language "${language}"`);
    // If a fallback is provided, use it instead of trying to find the key in English
    if (fallback !== null) {
      return fallback;
    }
    // Otherwise try to find it in English, or return the key itself as last resort
    return translations.english[key] || key;
  }
  
  return translations[language][key];
};

